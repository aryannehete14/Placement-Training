--Display product name, product category, total quantity sold, and total revenue generated for each product.
SELECT
    P.PROD_NAME,
    P.PROD_CATEGORY,
    SUM(S.QUANTITY_SOLD) AS TOTAL_QUANTITY,
    SUM(S.AMOUNT_SOLD)   AS TOTAL_REVENUE
FROM
         SH.SALES S
    JOIN SH.PRODUCTS P ON S.PROD_ID = P.PROD_ID
GROUP BY
    P.PROD_NAME,
    P.PROD_CATEGORY;

--Identify the top 10 customers contributing the highest sales revenue across all channels.
SELECT
    C.CUST_FIRST_NAME
    || ' '
       || C.CUST_LAST_NAME AS CUSTOMER_NAME,
    SUM(S.AMOUNT_SOLD)  AS TOTAL_REVENUE
FROM
         SH.SALES S
    JOIN SH.CUSTOMERS C ON S.CUST_ID = C.CUST_ID
GROUP BY
    C.CUST_FIRST_NAME,
    C.CUST_LAST_NAME
ORDER BY
    TOTAL_REVENUE DESC
FETCH FIRST 10 ROWS ONLY;
