SELECT
    *
FROM
    SH.PRODUCTS;

-- Display customer names in uppercase along with their country ID.
SELECT
    UPPER(CUST_FIRST_NAME
          || ' ' || CUST_LAST_NAME) AS CUSTOMER_NAME,
    COUNTRY_ID
FROM
    SH.CUSTOMERS;

-- Display all products where the minimum price has not been defined.
SELECT
    *
FROM
    SH.PRODUCTS
WHERE
    PROD_MIN_PRICE IS NULL;

--Display all products ordered by list price from highest to lowest.
SELECT
    *
FROM
    SH.PRODUCTS
ORDER BY
    PROD_LIST_PRICE DESC;

-- Find the total number of customers available in the customer master.
SELECT
    COUNT(*) AS TOTAL_CUSTOMERS
FROM
    SH.CUSTOMERS;

-- Display customers whose first name starts with the letter 'A'.
SELECT
    *
FROM
    SH.CUSTOMERS
WHERE
    CUST_FIRST_NAME LIKE 'A%';

-- Find the total sales amount generated through each sales channel.
SELECT
    C.CHANNEL_DESC,
    SUM(S.AMOUNT_SOLD) AS TOTAL_SALES
FROM
         SH.SALES S
    JOIN SH.CHANNELS C ON S.CHANNEL_ID = C.CHANNEL_ID
GROUP BY
    C.CHANNEL_DESC;

-- Display customer name, city, and country name.
